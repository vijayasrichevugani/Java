public class RelationalOperators {
    public static void main(String[] args) {
        int a = 30;
        int b = 15;
        int c = 50;
        int d = 30;
        System.out.println("== operator "+(a==d));
        System.out.println("!= operator "+(a!=c));
        System.out.println(">= operator "+(a>=c));
        System.out.println("<= operator "+(a<=c));
        System.out.println("> operator "+(b>d));
        System.out.println("< operator "+(b<d));

    }
}
